type Status = "idle" | "loading" | "success" | "error" | "limit";
type Result = "success" | "error" | "limit";

export default function ResendEmailButton() {
  const [status, setStatus] = useState<Status>("idle");
  const [seconds, setSeconds] = useState<number>(0);

  // countdown
  useEffect(() => {
    if (seconds <= 0) return;

    const interval = setInterval(() => {
      setSeconds((s) => s - 1);
    }, 1000);

    return () => clearInterval(interval);
  }, [seconds]);

  // reset to idle when timer finishes
  useEffect(() => {
    if (seconds === 0 && status !== "idle" && status !== "loading") {
      setStatus("idle");
    }
  }, [seconds, status]);

  const handleSend = async () => {
    if (status !== "idle") return;

    setStatus("loading");

    try {
      const email = sessionStorage.getItem("resendEmail");
      const storedData = sessionStorage.getItem("resendFormData");
      const successData = sessionStorage.getItem("formSuccessData");

      // Try resendFormData first, fall back to formSuccessData
      let formData: Record<string, string> | null = null;
      let userEmail = email;

      if (storedData) {
        formData = JSON.parse(storedData);
      } else if (successData) {
        const parsed = JSON.parse(successData);
        formData = parsed;
        userEmail = userEmail || parsed.email;
      }

      if (!userEmail || !formData) {
        setStatus("error");
        setSeconds(45);
        return;
      }

      const payload = {
        email: userEmail,
        reference: formData.reference || "",
        prenom: formData.prenom || "",
        nom: formData.nom || "",
        productName: formData.productName || "",
        montant: formData.montant || formData.primedelassistance || "",
      };
      const res = await fetch("/api/resend-virement", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload),
      });

      if (res.ok) {
        setStatus("success");
        setSeconds(45);
      } else if (res.status === 429) {
        setStatus("limit");
        setSeconds(600);
      } else {
        setStatus("error");
        setSeconds(600);
      }
    } catch {
      setStatus("error");
      setSeconds(600);
    }
  };

  if (status === "loading") {
    return (
      <button
        type="button"
        className="flex-center pointer-events-none w-[162px] py-2 px-4 rounded-full bg-Sage-Gray-Lower button-s"
      >
        <IconNameCircles />
      </button>
    );
  }

  if (status === "success") {
    return (
      <div className="f-col items-start gap-1">
        <div className="f-col gap-2">
          <span className="button-s text-Sage-Gray-High">
            Réessayez dans {seconds >= 60 ? `${Math.ceil(seconds / 60)} min` : `${seconds} s`}
          </span>

          <button
            type="button"
            className="flex pointer-events-none items-center gap-1 py-2 px-4 rounded-full bg-Sage-Gray-Medium button-s"
          >
            <span>Renvoyer l&apos;email</span>
            <IconRefresh className="shrink-0" />
          </button>
        </div>

        <div className="flex items-center gap-1">
          <IconNamecheckcirclefill />
          <span className="button-s text-Brand-500">
            Email renvoyé avec succès
          </span>
        </div>
      </div>
    );
  }

  if (status === "error") {
    return (
      <div className="f-col items-start gap-1">
        <div className="f-col gap-2">
          <span className="button-s text-Secondary-Red-Low">
            Réessayez dans {seconds >= 60 ? `${Math.ceil(seconds / 60)} min` : `${seconds} s`}
          </span>

          <button
            type="button"
            className="flex items-center gap-1 py-2 px-4 rounded-full bg-Sage-Gray-Lower opacity-50 cursor-not-allowed button-s"
          >
            <span>Renvoyer l&apos;email</span>
            <IconRefresh className="shrink-0" />
          </button>
        </div>

        <div className="flex items-center gap-1">
          <IconNamealertfill />
          <span className="button-s text-Secondary-Red-Medium">
            Impossible d’envoyer l’email. Réessayez plus tard.
          </span>
        </div>
      </div>
    );
  }

  if (status === "limit") {
    return (
      <div className="f-col items-start gap-1">
        <div className="f-col gap-2">
          <span className="button-s text-Sage-Gray-High">
            Réessayez dans {seconds >= 60 ? `${Math.ceil(seconds / 60)} min` : `${seconds} s`}
          </span>

          <button
            type="button"
            className="flex items-center gap-1 py-2 px-4 rounded-full bg-Sage-Gray-Lower opacity-50 cursor-not-allowed button-s"
          >
            <span>Renvoyer l&apos;email</span>
            <IconRefresh className="shrink-0" />
          </button>
        </div>

        <div className="flex items-center gap-1">
          <IconNameclosecirclefill />
          <span className="button-s">
            Vous avez déjà demandé plusieurs envois. Merci de patienter.
          </span>
        </div>
      </div>
    );
  }

  return (
    <button
      type="button"
      onClick={handleSend}
      className="flex items-center gap-1 py-2 px-4 rounded-full bg-Sage-Gray-Lower transition hover:bg-Sage-Gray-Low cursor-pointer button-s"
    >
      <span>Renvoyer l&apos;email</span>
      <IconRefresh className="shrink-0" />
    </button>
  );
}
